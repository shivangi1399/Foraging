def get_grouped_bare_paths(annotation_file, base_path):
    annotation = pd.read_csv(annotation_file)
    annotation['group_ID'] = annotation.groupby(['subject', 'date', 'moved_probe']).ngroup()
    grouped_bare_paths = []

    for group_id in set(annotation['group_ID']):
        group_data = annotation[annotation['group_ID'] == group_id]
        paths = []
        for _, row in group_data.iterrows():
            subject_folder = str(row['subject']).zfill(3)  # Assuming subject is padded with zeros
            session_folder = str(row['session']).zfill(3)  # Assuming session is padded with zeros
            date_str = str(row['date'])
            path = os.path.join(base_path, row['project'], subject_folder, date_str, row['experiment'], session_folder)
            paths.append(path)
        grouped_bare_paths.append(paths)

    return grouped_bare_paths

import os

def filter_grouped_paths(grouped_bare_paths, cronjob_paths):
    """
    Filters the grouped bare paths to include only those paths that have a common base with the normalized cronjob paths.

    :param grouped_bare_paths: List of lists of bare paths generated by get_grouped_bare_paths.
    :param cronjob_paths: List of full paths passed from the cronjob.
    :return: Filtered list of grouped bare paths.
    """
    filtered_grouped_paths = []

    # Normalize cronjob paths to ensure consistency
    normalized_cronjob_paths = [os.path.normpath(path) for path in cronjob_paths]

    for group in grouped_bare_paths:
        # Normalize each path in the group
        normalized_group = [os.path.normpath(path) for path in group]

        # Include the group if any of its normalized paths match the normalized cronjob paths
        if any(path in normalized_cronjob_paths for path in normalized_group):
            filtered_grouped_paths.append(group)

    return filtered_grouped_paths

def collect_nwb_files_and_identifier(bare_paths):
    nwb_paths = [glob.glob(f"{path}/**/*.nwb", recursive=True) for path in bare_paths]
    nwb_paths = sorted(sum(nwb_paths, []), key=lambda session_path: p.Path(session_path).parent.name)
    session_names = [p.Path(path).parent.name for path in nwb_paths]
    identifier = "-".join(session_names)
    return nwb_paths, identifier

